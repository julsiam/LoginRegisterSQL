    private void ipusernameFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ipusernameFocusGained
        if (ipusername.getText().trim().toLowerCase().equals("username")) {
            ipusername.setText("");
        }
    }//GEN-LAST:event_ipusernameFocusGained

    private void ipusernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ipusernameFocusLost
        // if the text field is equal to username or empty
        // we will set the "username" text in the field
        // on focus lost event

        if (ipusername.getText().trim().equals("")
            || ipusername.getText().trim().toLowerCase().equals("username")) {
            ipusername.setText("username");
        }
    }//GEN-LAST:event_ipusernameFocusLost

    private void ippasswordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ippasswordFocusGained
        // clear the password field on focus if the text is "password"

        // get the password text
        String pass = String.valueOf(ippassword.getPassword());

        if (pass.trim().toLowerCase().equals("password")) {
            ippassword.setText("");
        }
    }//GEN-LAST:event_ippasswordFocusGained

